  lang$ = new BehaviorSubject<Lang>(this.getInitialLang());

  private getInitialLang(): Lang {
    const stored = localStorage.getItem(this.storageKey) as Lang | null;
    if (stored === 'en' || stored === 'tr') return stored;
    return 'tr';
  }

  setLang(lang: Lang) {
    this.lang$.next(lang);
    localStorage.setItem(this.storageKey, lang);
    // Tüm uygulamayı yeniden render et
    this.appRef.tick();
  }

  translate(key: string): string {
    const lang = this.lang$.value;
    return this.dictionaries[lang][key] ?? key;
  }
